    private void btnEliminarDisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarDisActionPerformed
        if (foco.eliminarDiscipulado(foco)){
                JOptionPane.showMessageDialog(this, "Discipulado eliminado exitosamente!");
                for (int i=0; i<aLD.size(); i++){
                    if (foco.getIdDiscipulado()== aLD.get(i).getIdDiscipulado()){
                        aLD.remove(i);
                        actListaDis();  
                    }
                }
            }else{
                JOptionPane.showMessageDialog(this, "No se pudo eliminar Discipulado"
                        , "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_btnEliminarDisActionPerformed

    private void lDiscipuladoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lDiscipuladoMouseClicked
        try{
            int posicion = lDiscipulado.locationToIndex(evt.getPoint());
            foco = aLD.get(posicion);
            btnEditarDis.setEnabled(true);
            btnVerDis.setEnabled(true);
            btnEliminarDis.setEnabled(true);
        }catch(Exception e){}
    }//GEN-LAST:event_lDiscipuladoMouseClicked

    private void actListaDis(){
        lista1 = new DefaultListModel();
        this.lDiscipulado.setModel(lista1);

            for( int i = 0 ; i < aLD.size() ; i++ ){
                Congregante c = new Congregante ();
                c = c.buscarC(aLD.get(i).getInstructor(), aLC);
                lista1.addElement("<html><font color='blue' face='Comic Sans MS' size='4'>"+aLD.get(i).getNombre()+"</font><br><b>Instructor: </b>"
                        + ""+c.getNombres()+" "+c.getApellidos()+"</html>");
            }
        btnEditarDis.setEnabled(false);
        btnVerDis.setEnabled(false);
        btnEliminarDis.setEnabled(false);
    }

