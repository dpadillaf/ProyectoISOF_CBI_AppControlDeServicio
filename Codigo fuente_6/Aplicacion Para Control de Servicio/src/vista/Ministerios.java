    private void listaMinisterioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaMinisterioMouseClicked
        try{
            int posicion = listaMinisterio.locationToIndex(evt.getPoint());
            foco = aLM.get(posicion);
            btnEditarMin.setEnabled(true);
            btnVerMin.setEnabled(true);
            btnEliminarMin.setEnabled(true);
        }catch(Exception e){}
    }//GEN-LAST:event_listaMinisterioMouseClicked

    private void actListaMin(){
        lista1 = new DefaultListModel();
        this.listaMinisterio.setModel(lista1);

            for( int i = 0 ; i < aLM.size() ; i++ ){
                Congregante c = new Congregante ();
                c = c.buscarC(aLM.get(i).getLider(), aLC);
                lista1.addElement("<html><font color='blue' face='Comic Sans MS' size='4'>"+aLM.get(i).getNombre()+"</font><br><b>Lider: </b>"
                        + ""+c.getNombres()+" "+c.getApellidos()+"</html>");
            }
        btnEditarMin.setEnabled(false);
        btnVerMin.setEnabled(false);
        btnEliminarMin.setEnabled(false);
    }
    

