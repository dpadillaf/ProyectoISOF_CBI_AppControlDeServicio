    private void btnEliminarMinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMinActionPerformed
            if (foco.eliminarMinisterio(foco)){
                JOptionPane.showMessageDialog(this, "Ministerio eliminado exitosamente!");
                for (int i=0; i<aLM.size(); i++){
                    if (foco.getCodigo() == aLM.get(i).getCodigo()){
                        aLM.remove(i);
                        actListaMin();    
                    }
                }
            }else{
                JOptionPane.showMessageDialog(this, "No se pudo eliminar Ministerio"
                        , "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_btnEliminarMinActionPerformed

