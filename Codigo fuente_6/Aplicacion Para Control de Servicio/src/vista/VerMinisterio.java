    private void btnEMiembrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEMiembrosActionPerformed
        if (min.eliminarMiembro(min.getCodigo(), focoMie)){
                JOptionPane.showMessageDialog(this, "Miembro eliminado exitosamente!");
                Vector<Long> v = min.getMiembros();
                for (int i=0; i<min.getMiembros().size(); i++){
                    if (focoMie == min.getMiembros().get(i)){
                        v.remove(i);
                        min.setMiembros(v);
                        agregaListaMiembros ();    
                    }
                }
            }else{
                JOptionPane.showMessageDialog(this, "No se pudo eliminar Ministerio"
                        , "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_btnEMiembrosActionPerformed

