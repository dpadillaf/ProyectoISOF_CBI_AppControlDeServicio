    private void btnAMiembroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAMiembroActionPerformed
        AgregarMiembrosMinisterio amm = new AgregarMiembrosMinisterio(null, true, aLC, min);
        amm.setVisible(true);
        Iglesia ig = new Iglesia();
        for (int i=0; i<ig.getListaMinisterios().size(); i++){
            if (ig.getListaMinisterios().get(i).getCodigo() == min.getCodigo()){
                min = ig.getListaMinisterios().get(i);
            }
        }
        agregaListaMiembros ();
    }//GEN-LAST:event_btnAMiembroActionPerformed

