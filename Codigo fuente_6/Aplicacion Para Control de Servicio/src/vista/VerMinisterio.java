    private void btnEActividadesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEActividadesActionPerformed
        if (focoAct.eliminarActividad(min.getCodigo(), focoAct.getCodActividad())){
                JOptionPane.showMessageDialog(this, "Actividad eliminada exitosamente!");
                ArrayList<ActividadMinisterio> aLAM = min.getActividades();
                for (int i=0; i<min.getActividades().size(); i++){
                    if (focoAct.getCodActividad() == min.getActividades().get(i).getCodActividad()){
                        aLAM.remove(i);
                        min.setActividades(aLAM);
                        actListaActividades();    
                    }
                }
            }else{
                JOptionPane.showMessageDialog(this, "No se pudo eliminar Ministerio"
                        , "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_btnEActividadesActionPerformed

