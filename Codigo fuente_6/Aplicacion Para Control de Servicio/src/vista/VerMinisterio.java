    private void agregaListaMiembros (){
        lista1 = new DefaultListModel();
        this.listaMiembros.setModel(lista1);

            for( int i = 0 ; i < min.getMiembros().size() ; i++ ){
                Congregante c = new Congregante ();
                c = c.buscarC(min.getMiembros().get(i), aLC);
                lista1.addElement("<html><font color='blue' face='Comic Sans MS' size='4'>"+c.getNombres()+" "+c.getApellidos()+"</font><br><b>Identificación: </b>"
                        + ""+c.getNumIdentificacion()+"</html>");
            }
            btnEMiembros.setEnabled(false);
    }
    
    private void actListaActividades(){
        lista2 = new DefaultListModel();
        this.listaActividades.setModel(lista2);

            for( int i = 0 ; i < min.getActividades().size() ; i++ ){
                lista2.addElement("<html><font color='blue' face='Comic Sans MS' size='4'>"+min.getActividades().get(i).getNombre()+"</font><br><b>Fecha: </b>"
                        + ""+new SimpleDateFormat("dd-MM-yyyy").format(min.getActividades().get(i).getFecha())+"<br><b>Descripción: </b>"+min.getActividades().get(i).getDescripcion()+"</html>");
            }
            btnEActividades.setEnabled(false);
    }

