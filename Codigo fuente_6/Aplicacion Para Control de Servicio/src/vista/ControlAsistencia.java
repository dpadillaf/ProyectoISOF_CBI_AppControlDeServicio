    private void agregaListaAsistencia (){
        try{
        lista1 = new DefaultListModel();
        this.listaAsistencia.setModel(lista1);
            //System.out.println(lAsistencia.size());
            for( int i = 0 ; i < lAsistencia.size() ; i++ ){
                Date m = lAsistencia.get(i).getFecha();
                Date n = fechaAsistencia.getDate();
                if (m.getDate()==n.getDate() && m.getMonth()==n.getMonth() && m.getYear()==n.getYear()){
                    for (int j=0; j<lAsistencia.get(i).getAsistentes().size(); j++){
                        Congregante c = new Congregante ();
                        c = c.buscarC(lAsistencia.get(i).getAsistentes().get(j), lCongregante);
                        lista1.addElement("<html><font color='blue' face='Comic Sans MS' size='4'>"+c.getNombres()+" "+c.getApellidos()+"</font><b> -- </b>"
                        + ""+c.getNumIdentificacion()+"</html>");
                    }
                }
            }
        }catch(Exception e){}
    }
    
