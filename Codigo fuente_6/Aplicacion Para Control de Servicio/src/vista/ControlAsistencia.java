    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Congregante c = new Congregante ();
        if (c.buscarC(Long.parseLong(jTID.getText()), lCongregante) != null){
            Asistencia nueva = new Asistencia (); 
            boolean ver = nueva.agregaAsistencia(Long.parseLong(jTID.getText()), new Date());
            if (ver == false){
                JOptionPane.showMessageDialog(this, "No se pudo agregar a la base de datos, revise si "
                        + "existe congregante con ese número", "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Asistencia agregado exitosamente!");
                Iglesia ig = new Iglesia ();
                lAsistencia = ig.getListaAsistencia();
                agregaListaAsistencia ();
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

